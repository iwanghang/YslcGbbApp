package com.gzyslczx.yslc.tools.yourui;

public class DailyMAEntity {

    private float MA5=-1, MA10=-1, MA20=-1, MA30=-1;
    private int date;

    public float getMA5() {
        return MA5;
    }

    public void setMA5(float MA5) {
        this.MA5 = MA5;
    }

    public float getMA10() {
        return MA10;
    }

    public void setMA10(float MA10) {
        this.MA10 = MA10;
    }

    public float getMA20() {
        return MA20;
    }

    public void setMA20(float MA20) {
        this.MA20 = MA20;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public float getMA30() {
        return MA30;
    }

    public void setMA30(float MA30) {
        this.MA30 = MA30;
    }
}
